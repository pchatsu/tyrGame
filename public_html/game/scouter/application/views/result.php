<script>
var rotatetimer;

function visible(id){
	document.getElementById("target"+id).style.border="1px solid red";
	document.getElementById("description"+id).style.visibility="visible";
	if(id==4){
		rotate();
	}
}
function unVisible(id){
	document.getElementById("target"+id).style.border="none";
	document.getElementById("description"+id).style.visibility="hidden";
	if(id==4){
		clearInterval(rotatetimer);
	}
}
function boost(){
	var power=document.getElementById("power_value").innerHTML;
	var offset=837;
	timer=setInterval(function(){
		if(parseInt(power)>99999000){
			power=99999999;
			document.getElementById("power_value").innerHTML=power;
			clearInterval(timer);
			explode();
		}else{
			offset=parseInt(offset*1.2);
			power=parseInt(power)+offset;
			document.getElementById("power_value").innerHTML=power;
		}
	},50);
}
function explode(){
	document.getElementById("date").style.visibility="hidden";
	document.getElementById("power").style.visibility="hidden";
	document.getElementById("scouter_cover").style.visibility="hidden";
	document.getElementById("descriptions").style.visibility="hidden";
	document.getElementById("targets").style.visibility="hidden";
	document.getElementById("scouter").style.background="url(img/explosion.png) no-repeat 260px 0,url(img/scouter_broken.png) no-repeat 0 50px";
	timer = setTimeout(function(){
		document.getElementById("scouter").style.background="url(img/scouter_broken.png) no-repeat 0 50px";
		clearInterval(timer);
	},500); 
}
function onoff(id){
	
	count=1;
	setInterval(function(){
		if(count===1){
			document.getElementById(id).style.visibility="visible";
		}else if(count===6){
			document.getElementById(id).style.visibility="hidden";
			count=0;
		}
			count++;
	},250); 

}
function slot(id,n){
	var text=document.getElementById(id).innerHTML;
	var count=0;
	var timer=setInterval(function(){
		var a =	'ABCDEFGHIJKLMNOPQRSTUVWXYZ';
		a = a.split('');
		document.getElementById(id).innerHTML=a[Math.floor(Math.random() * a.length)];
		if(count===n){
			document.getElementById(id).innerHTML=text;
			clearInterval(timer);
		}
		count++;
	},100);
}
function rotate(){
	var imgNum = 5;	
	var imgSize = 400;
	var time = 2000;
	var current = 1;

	if(document.getElementById("friends_num").innerHTML!=null && parseInt(document.getElementById("friends_num").innerHTML)/6<5){
		friendsNum = parseInt(document.getElementById("friends_num").innerHTML);
		imgNum = (friendsNum-1)/6+1;
		for(var i=friendsNum+1;i<=6*imgNum;i++ ){
			document.getElementById("friend"+i).style.visibility="hidden";
			document.getElementById("friend"+i+"_name").style.visibility="hidden";
		}
	}

	if(imgNum === 1){
		return;
	}

	$("#pages").css("left","0px");
	rotatetimer = setInterval(function(){
		if( current == imgNum ) {
			$("#pages").animate({
				//left : parseInt($("#pages").css("left"))-imgSize+"px"
				left : -imgSize*current+"px"
			},"slow", function() {
				$("#pages").css("left","0px");
				current=1;
			});
		}else{	
			$("#pages").animate({
				//left : parseInt($("#pages").css("left"))-imgSize+"px"
				left : -imgSize*current+"px"
			},"slow");
			current++;
		}
	}, time);
}
$(function(){
document.getElementById("title").style.visibility="hidden";
setTimeout(function(){
	document.getElementById("title").style.visibility="visible";
	slot("title1",10);
	slot("title2",15);
	slot("title3",20);
	slot("title4",25);
	slot("title5",30);
	slot("title6",35);
	slot("title7",40);
	setTimeout(function(){
		onoff("title");
	},4000);
},2000);
});
</script>

